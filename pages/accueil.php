<h1 class="titre">History Escape</h1><h1>Saurez-vous échapper aux pièges et aux énigmes de l'Histoire ?</h1>
<p><span>L</span>e Moyen-Âge recèle nombre de contes et légendes encore vivants aujourd'hui. 
    Des histoires remplies de chevaliers, de quêtes, de créatures imaginaires et d'action, qui fascinent autant qu'elles intriguent.</br>
Chez History Escape, nous nous sommes donnés une mission : redonner vie à cette époque et ses légendes au sein d'une escape room.
C'est l'occasion d'une plongée dans les arcanes du temps, à la découverte d'un passé oublié et mystérieux.</p>
